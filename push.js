// Syntax 
// push(element0)
// push(element0, element1)
// push(element0, element1, ... , elementN)

// Example 1
let animals = ['cats', 'birds', 'dogs', 'goats']
animals.push('tiger')
console.log(animals)

// Example 2
let arr = [10, 9, 8, 7, 6]
arr.push(5, 10)
console.log(arr)

// Example 3
let animalSounds = ['roar', 'baaa', 'roof']
animalSounds.push('meow')
console.log(animalSounds)