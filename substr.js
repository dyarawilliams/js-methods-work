// Syntax
// substr(start)
// substr(start, length)

// Example 1
const str = 'Javascript'
console.log(str.substr(0, 4)) // Expected Output: "Java"

// Example 2
const str2 = 'Composition'
console.log(str2.substr(3)) // Expected Output: "position"

// Example 3
const str3 = 'It is a beautiful day.'
console.log(str3.substr(5, 16)) // Expected Output: " a beautiful day"