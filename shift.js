// Syntax 
// shift()

// Example 1
const arr1 = [3, 2, 3]
const firstElem = arr1.shift()
console.log(firstElem) // Expected Output: 3

// Example 2
const arr2 = ['hello', 'welcome', 'world']
const firstElem2 = arr2.shift()
console.log(firstElem2) // Expected Output: "hello"

// Example 3
const arr3 = ['fruits', 'grapes', 'peaches', 'apples']
const firstElem3 = arr3.shift()
console.log(firstElem3) // Expected Output: "fruits"