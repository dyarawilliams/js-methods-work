// Syntax 
// toUpperCase()

// Example 1
const sentence =  'Today is going to be fun.!'
console.log(sentence.toUpperCase()) // Expected Output: "TODAY IS GOING TO BE FUN.!"

// Example 2
const str1 = 'the cow jumped over the moon.'
console.log(str1.toUpperCase()) // Expected Output: "THE COW JUMPED OVER THE MOON."

// Example 3
const str2 = 'Time to search one by one.'
console.log(str2.toUpperCase()) // Expected Output: "TIME TO SEARCH ONE BY ONE."