// toLowerCase()

// Example 1
const str = 'HELLO, WELCOME TO JAVASCRIPT'
const result = str.toLowerCase()
console.log(result) // Expected Output: "hello, welcome to javascript"

// Example 2
const str2 = 'Can we Go shoPPing?'
const result2 = str2.toLowerCase()
console.log(result2) // Expected Output: "can we go shopping?"

