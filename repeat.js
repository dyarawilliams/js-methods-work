// Syntax 
// repeat(count)

// Example 1
const greeting =  'Welcome World.! '
console.log(`Greeting: ${greeting.repeat(7)}`) // Expected Output: "Greeting: Welcome World.! Welcome World.! Welcome World.! Welcome World.! Welcome World.! Welcome World.! Welcome World.! "

// Example 2
const str = 'Edward Scissor hands is not scary! '
console.log(str.repeat(3)) // Expected Output: "Edward Scissor hands is not scary! Edward Scissor hands is not scary! Edward Scissor hands is not scary! "

// Example 3
const game = 'Best Game Ever! '
console.log(`Apex is the, ${game.repeat(2)}`) // Expected Output: "Apex is the, Best Game Ever!Best Game Ever!"

